class Solution{
  public:
    int thirdLargest(int a[], int n)
    {
         //Your code here
         vector<int>v;
         for(int i=0;i<n;i++)
         {
             v.push_back(a[i]);
         }
         sort(v.begin(),v.end());
         return v[v.size()-3];
    }

};

